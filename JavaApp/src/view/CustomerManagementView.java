    /**
     * Thiet lap (dinh nghia) cac cot cho bang customerTable
     */
    private void setupTable() {
        // (Ten bien 'customerTable' phai khop voi ten ban dat)
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Khong cho phep sua truc tiep tren bang
            }
        };
        model.addColumn("ID Khach Hang");
        model.addColumn("So Dien Thoai");
        model.addColumn("Ho Va Ten");
        customerTable.setModel(model);
        customerTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    /**
     * Tai danh sach khach hang tu Controller va do vao bang
     */
    private void loadCustomersTable() {
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0); // Xoa het du lieu cu
        
        List<Customer> customers = customerController.getAllCustomers();
        for (Customer c : customers) {
            model.addRow(new Object[]{
                c.getId(),
                c.getPhone(),
                c.getFullName()
            });
        }
        
        // Sau khi tai lai, reset lua chon
        clearFields();
    }
    
    /**
     * Them su kien click vao bang
     * Khi click 1 dong, du lieu se duoc luu lai vao bien selectedCustomerId
     */
    private void addTableClickListener() {
        // (Ten bien 'customerTable' phai khop voi ten ban dat)
        customerTable.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int selectedRow = customerTable.getSelectedRow();
                if (selectedRow == -1) return; // Neu khong chon dong nao

                // Lay du lieu tu bang (Model)
                int id = (int) customerTable.getValueAt(selectedRow, 0);
                String name = (String) customerTable.getValueAt(selectedRow, 2);

                // Luu lai ID da chon
                selectedCustomerId = id;
                // (Ten bien 'lblSelectedCustomer' phai khop voi ten ban dat)
                lblSelectedCustomer.setText("Da chon: " + name + " (ID: " + id + ")");
            }
        });
    }

    /**
     * Ham tien ich de dat lai lua chon
     */
    
    private void clearFields() {
        selectedCustomerId = -1;
        // (Ten bien 'lblSelectedCustomer' phai khop voi ten ban dat)
        lblSelectedCustomer.setText("Chua chon khach hang nao.");
        customerTable.clearSelection(); // Bo chon dong
    }
    
