    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        // (Code khong dau de tranh loi font)
        // 1. Kiem tra xem nguoi dung da chon khach hang chua
        if (selectedCustomerId == -1) {
            JOptionPane.showMessageDialog(this, "Vui long chon mot khach hang tu bang de xoa!", "Chua Chon Khach Hang", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Hoi xac nhan
        int confirm = JOptionPane.showConfirmDialog(this,
            "Ban co chac chan muon xoa khach hang co ID = " + selectedCustomerId + " khong?\n(Luu y: Khong the xoa khach hang da co don hang)",
            "Xac Nhan Xoa",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            // 3. Goi "bo nao" (Controller) de xu ly
            boolean success = customerController.deleteCustomer(selectedCustomerId);

            // 4. Xu ly ket qua
            if (success) {
                JOptionPane.showMessageDialog(this, "Xoa khach hang thanh cong!", "Thanh Cong", JOptionPane.INFORMATION_MESSAGE);
                loadCustomersTable(); // Tai lai bang
            } else {
                JOptionPane.showMessageDialog(this, "Xoa that bai!\n(Loi: Khach hang nay da co don hang hoac ID khong hop le)", "Loi Khoa Ngoai", JOptionPane.ERROR_MESSAGE);
            }
        }

        // 5. Dat lai lua chon
        clearFields();
    }//GEN-LAST:event_btnDeleteActionPerformed

