    /**
     * Thiet lap (dinh nghia) cac cot cho bang profitTable
     */
    private void setupTable() {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Khong cho phep sua truc tiep tren bang
            }
        };
        model.addColumn("ID");
        model.addColumn("Ngay");
        model.addColumn("Doanh Thu");
        model.addColumn("Chi Phi Thit");
        model.addColumn("Chi Phi Gia Vi");
        model.addColumn("Chi Phi La");
        model.addColumn("Chi Phi Nhan Cong");
        model.addColumn("LOI NHUAN RONG");
        profitTable.setModel(model);
        profitTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    /**
     * Tai danh sach ban ghi loi nhuan tu Controller va do vao bang
     */
    private void loadProfitTable() {
        DefaultTableModel model = (DefaultTableModel) profitTable.getModel();
        model.setRowCount(0); // Xoa het du lieu cu
        
        List<Profit> profits = profitController.getAllProfitRecords();
        for (Profit p : profits) {
            model.addRow(new Object[]{
                p.getId(),
                p.getDate(),
                currencyFormatter.format(p.getRevenue()), // Dinh dang tien te
                currencyFormatter.format(p.getCostMeat()),
                currencyFormatter.format(p.getCostSpices()),
                currencyFormatter.format(p.getCostLeaf()),
                currencyFormatter.format(p.getCostLabor()),
                currencyFormatter.format(p.getNetProfit()) // Dinh dang tien te
            });
        }
    }
    
