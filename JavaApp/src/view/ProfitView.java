    private void btnAddProfitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProfitActionPerformed
        // TODO add your handling code here:
        // (Code khong dau de tranh loi font)
        try {
            // 1. Lay du lieu tu cac o text field
            // (Chuyen doi chuoi rong thanh "0" de tranh loi NumberFormatException)
            double revenue = Double.parseDouble(txtRevenue.getText().isEmpty() ? "0" : txtRevenue.getText());
            double costMeat = Double.parseDouble(txtCostMeat.getText().isEmpty() ? "0" : txtCostMeat.getText());
            double costSpices = Double.parseDouble(txtCostSpices.getText().isEmpty() ? "0" : txtCostSpices.getText());
            double costLeaf = Double.parseDouble(txtCostLeaf.getText().isEmpty() ? "0" : txtCostLeaf.getText());
            double costLabor = Double.parseDouble(txtCostLabor.getText().isEmpty() ? "0" : txtCostLabor.getText());

            // Lay ngay hom nay
            Date today = new Date();

            // 2. Goi "bo nao" (Controller) de xu ly
            boolean success = profitController.addProfitRecord(today, revenue, costMeat, costSpices, costLeaf, costLabor);

            // 3. Xu ly ket qua
            if (success) {
                JOptionPane.showMessageDialog(this, "Them ban ghi loi nhuan thanh cong!", "Thanh Cong", JOptionPane.INFORMATION_MESSAGE);
                loadProfitTable(); // Tai lai bang
                clearFields(); // Xoa trang form
            } else {
                JOptionPane.showMessageDialog(this, "Them that bai! (Kiem tra cac so lieu khong duoc am)", "Loi", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui long nhap dung dinh dang so (Number) cho cac o!", "Loi Dinh Dang", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Da xay ra loi: " + e.getMessage(), "Loi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddProfitActionPerformed

